/**
 * 根据年龄段筛选适合的食谱
 */
export function filterRecipesByAge(recipes, ageInMonths) {
  const ageInYears = ageInMonths / 12
  return recipes.filter(r => {
    const minAge = r.ageRange[0]
    const maxAge = r.ageRange[1]
    return ageInYears >= minAge && ageInYears <= maxAge
  })
}

/**
 * 根据食物禁忌筛选食谱
 */
export function filterRecipesByAllergies(recipes, allergies = [], disliked = []) {
  const forbidden = [...new Set([...allergies, ...disliked].map(a => a.trim().toLowerCase()))]
  if (forbidden.length === 0) return recipes
  return recipes.filter(r => {
    const recipeIngredients = r.ingredients.map(i => i.toLowerCase())
    return !forbidden.some(f => recipeIngredients.some(i => i.includes(f)))
  })
}

/**
 * 按餐类型获取食谱
 */
export function getRecipesByMealType(recipes, mealType) {
  return recipes.filter(r => r.mealType === mealType)
}
